package com.operator;

public class RelationalOprators {

	public static void main(String[] args) {
		int a = 10; 
		int b = 25;
		
		System.out.println("a < b  : "+ (a < b)); 
		System.out.println("a > b  : "+ (a > b));
		System.out.println("a <= b : "+ (a <= b));
		System.out.println("a >= b : "+ (a >= b)); 
		System.out.println("a == b : "+ (a == b)); //equal equal to
		System.out.println("a != b : "+ (a != b)); // not equal to
	}
}
